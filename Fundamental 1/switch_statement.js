
const day = "Thursday";

switch (day) {
    case "Monday":
        console.log("Three days ago was Monday");
        break;
    case "Tuesday":
        console.log("Two days ago was Tuesday");
        break;
    case "Wednesday":
        console.log("Yesterday was Wednesday");
        break;
    case "Thursday":
        console.log("Today's Thursday");
        break;
    case "Friday":
        console.log("Tomorrow's Friday");
        break;
    case "Saturday":
        console.log("Two days from now will be Saturday");
        break;
    case "Sunday":
        console.log("Three days from now will be Sunday");
        break;
        default:
        console.log("Invalid day of the week");
        break;

}